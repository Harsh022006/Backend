text = "PythonProgramming"

# Slice from index 0 to 6
print("Slice [0:6]:", text[0:6])

# Slice from index 6 to end
print("Slice [6:]:", text[6:])

# Slice from beginning to index 10
print("Slice [:10]:", text[:10])

# Slice the whole string
print("Slice [:]:", text[:])

# Slice with step value (every 2nd character)
print("Slice [::2]:", text[::2])
